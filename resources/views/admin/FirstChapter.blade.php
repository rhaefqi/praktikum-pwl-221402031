                                <h1>One Piece</h1><br>
                                <font size="4">
                                One piece adalah sebuah seri manga jepang yang ditulis oleh Eichiro oda. Manga ini telah dimuat di majalah weekly shonen jump milik shueisha
                                sejak tanggal 22 juli 1997, dan telah di bundel menjadi 103 volume tankobon hingga agustus 2022. ceritanya mengisahkan petualangan Monkey D. Luffy,
                                seorang anak laki laki yang berasal dari east blue yang secara tidak sengaja memakan buah iblis yang memberikan nya kemampuan tubuh elastis seperti karet,
                                Luffy bersama rekan bajak laut yang telah dikumpulkan nya dari berbagai wilayah dengan karakteristik dan kemampuan nya masing masing membentuk
                                kru bajak laut, yang dinamakan bajak laut topi jerami, mereka menjelajahi lautan untuk mencari harta karun terbesar di dunia yang dikenal sebagai "onepiece" 
                                dan untuk memenuhi impian mereka masing masing, serta memnuhi impian luffy sang kapten untuk menjadi seorang raja bajak laut
                                <br>
                                <br>
                                Manga ini telah diadaptasi menjadi sebuah animasi video asli (OVA) yang diproduksi oleh Production I.G pada tahun 1998, dan sebuah serial 
                                anime yang diproduksi oleh Toei Animation, yang mulai tayang di Jepang pada tahun 1999. Sebagai tambahan, Toei juga telah memproduksi 
                                tiga belas film animasi, satu OVA dan sebelas episode khusus televisi. Beberapa perusahaan telah mengembangkan berbagai jenis pernak-pernik
                                seperti permainan kartu dagang dan beberapa permainan video. Versi manganya telah dilisensi untuk dirilis dalam bahasa Inggris di Amerika Utara 
                                dan Britania Raya oleh Viz Media dan di Australia oleh Madman Entertainment. Versi animenya telah dilisensi oleh 4Kids Entertainment untuk perilisan
                                versi bahasa Inggris di Amerika Utara pada tahun 2004, sebelum lisensi tersebut dicabut dan kemudian diakuisisi oleh Funimation pada tahun 2007.
                                <br>
                                <br>
                                One Piece menerima pujian atas alur cerita, gambar, karakterisasi, dan humornya. Beberapa volume dari manga ini telah memecahkan 
                                rekor penerbitan, termasuk cetakan awal tertinggi dari buku apa pun di Jepang. Situs web resmi untuk manga One Piece karya Eiichiro Oda 
                                mengumumkan bahwa manga ini telah meraih Guinness World Record untuk kategori "salinan terbanyak yang diterbitkan untuk seri buku komik 
                                yang sama oleh seorang penulis tunggal". Hingga bulan Mei 2018,manga ini telah terjual hingga lebih dari 440 juta kopi di seluruh dunia,
                                menjadikannya seri manga terlaris dalam sejarah.One Piece juga menjadi manga terlaris selama sepuluh tahun berturut-turut pada tahun 2017.
                                <br>
                                <br>
                                Manga One Piece mulai diserialisasikan pada majalah Shonen Jump edisi #34 pada 4 Agustus 1997. Animenya mulai diproduksi oleh Toei Animation
                                di Fuji Television pada 20 Oktober 1999.Pada awalnya, Eiichiro Oda merencanakan One Piece akan berjalan sekitar 5 tahun, dan dia telah menetapkan 
                                endingnya. Tetapi dia terlalu "menikmati" jalan ceritanya dan sekarang dia tidak tahu kapan One Piece akan berakhir.
                                <br>
                                <br>
                                One Piece merupakan manga terlaris sepanjang sejarah Shonen Jump. Manga ini juga yang menyebabkan total penjualan Shonen Jump naik pada 11 tahun 
                                terakhir ini Volume 25 memegang rekor penjualan manga di Jepang, dengan 2.630.000 unit terjual pada cetakan pertama. Dan volume 46 mencapai lebih 
